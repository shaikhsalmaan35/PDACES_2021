// ----------var function-----------
function getDataVar(){
    if(true){
        var name="salman";
    }
    console.log(name);
}
getDataVar();


// ---------------Let function---------
function GetDataLet(){
    if(true){
        let name="shaikh";
        console.log(name);
    }
}
GetDataLet();

